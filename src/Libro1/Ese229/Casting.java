package Libro1.Ese229;

public class Casting {
    public static void main(String[] args) {
        System.out.printf("Il carattere %c ha valore %d\n",'A',(int)'A');
        System.out.printf("Il carattere %c ha valore %d\n",'B',(int)'B');
        System.out.printf("Il carattere %c ha valore %d\n",'C',(int)'C');
        System.out.printf("Il carattere %c ha valore %d\n",'a',(int)'a');
        System.out.printf("Il carattere %c ha valore %d\n",'b',(int)'b');
        System.out.printf("Il carattere %c ha valore %d\n",'c',(int)'c');
        System.out.printf("Il carattere %c ha valore %d\n",'0',(int)'0');
        System.out.printf("Il carattere %c ha valore %d\n",'1',(int)'1');
        System.out.printf("Il carattere %c ha valore %d\n",'2',(int)'2');
        System.out.printf("Il carattere %c ha valore %d\n",'$',(int)'$');
        System.out.printf("Il carattere %c ha valore %d\n",'*',(int)'*');
        System.out.printf("Il carattere %c ha valore %d\n",'+',(int)'+');
        System.out.printf("Il carattere %c ha valore %d\n",'/',(int)'/');
        System.out.printf("Il carattere %c ha valore %d\n",' ',(int)' ');
    }
}
